// The difference between null and undefined

// A variable is undefined if it's been declared but has not been assigned a value
let value1
console.log(value1) // undefined
console.log(typeof value1) // undefined

// Null itself is a value and can be assigned to variables
// Null means no value
let value2 = null
console.log(value2) // null
console.log(typeof value2) // object
